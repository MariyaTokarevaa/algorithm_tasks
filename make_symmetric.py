'''–ù–∞ –≤—Ö–æ–¥ –ø–æ–¥–∞—ë—Ç—Å—è –ø–æ—Å–ª–µ–¥–æ–≤–∞—Ç–µ–ª—å–Ω–æ—Å—Ç—å —á–∏—Å–µ–ª.
–ù–µ–æ–±—Ö–æ–¥–∏–º–æ –æ–ø—Ä–µ–¥–µ–ª–∏—Ç—å, –∫–∞–∫–æ–µ –º–∏–Ω–∏–º–∞–ª—å–Ω–æ–µ –∫–æ–ª–∏—á–µ—Å—Ç–≤–æ –∏ –∫–∞–∫–∏—Ö —á–∏—Å–µ–ª –Ω—É–∂–Ω–æ –¥–æ–ø–∏—Å–∞—Ç—å –≤ –∫–æ–Ω–µ—Ü –ø–æ—Å–ª–µ–¥–æ–≤–∞—Ç–µ–ª—å–Ω–æ—Å—Ç–∏,
 —á—Ç–æ–±—ã –æ–Ω–∞ —Å—Ç–∞–ª–∞ —Å–∏–º–º–µ—Ç—Ä–∏—á–Ω–æ–π.
 –§—É–Ω–∫—Ü–∏—è, —Ç–∞–∫–∏–º –æ–±—Ä–∞–∑–æ–º, –¥–æ–ª–∂–Ω–∞ –≤–æ–∑–≤—Ä–∞—â–∞—Ç—å —á–∏—Å–ª–æ (–∫–æ–ª–∏—á–µ—Å—Ç–≤–æ —á–∏—Å–µ–ª) –∏ —Å–ø–∏—Å–æ–∫ (–∫–∞–∫–∏–µ –∏–º–µ–Ω–Ω–æ —á–∏—Å–ª–∞).

üîÑ –°–∏–º–º–µ—Ç—Ä–∏—á–Ω–∞—è –ø–æ—Å–ª–µ–¥–æ–≤–∞—Ç–µ–ª—å–Ω–æ—Å—Ç—å ‚Äî —ç—Ç–æ —Ç–∞–∫–∞—è –ø–æ—Å–ª–µ–¥–æ–≤–∞—Ç–µ–ª—å–Ω–æ—Å—Ç—å,
–∫–æ—Ç–æ—Ä–∞—è —á–∏—Ç–∞–µ—Ç—Å—è –æ–¥–∏–Ω–∞–∫–æ–≤–æ —Å–ø—Ä–∞–≤–∞ –Ω–∞–ª–µ–≤–æ –∏ —Å–ª–µ–≤–∞ –Ω–∞–ø—Ä–∞–≤–æ. '''


def make_symmetric(seq):
    """
    –ù–∞—Ö–æ–¥–∏—Ç, —á—Ç–æ –¥–æ–±–∞–≤–∏—Ç—å –≤ –∫–æ–Ω–µ—Ü seq, —á—Ç–æ–±—ã –æ–Ω–∞ —Å—Ç–∞–ª–∞ –ø–∞–ª–∏–Ω–¥—Ä–æ–º–æ–º.

    Returns:
        tuple: (–∫–æ–ª–∏—á–µ—Å—Ç–≤–æ_—ç–ª–µ–º–µ–Ω—Ç–æ–≤_–¥–ª—è_–¥–æ–±–∞–≤–ª–µ–Ω–∏—è, —Å–ø–∏—Å–æ–∫_—ç–ª–µ–º–µ–Ω—Ç–æ–≤)
    """
    n = len(seq)
    # –ò—Ç–µ—Ä–∏—Ä—É–µ–º –ø–æ –≤—Å–µ–º –≤–æ–∑–º–æ–∂–Ω—ã–º —Ç–æ—á–∫–∞–º –Ω–∞—á–∞–ª–∞ "—Ö–≤–æ—Å—Ç–∞"
    for i in range(n):
        # –ü—Ä–æ–≤–µ—Ä—è–µ–º, —è–≤–ª—è–µ—Ç—Å—è –ª–∏ —Ç–µ–∫—É—â–∏–π "—Ö–≤–æ—Å—Ç" (seq[i:]) –ø–∞–ª–∏–Ω–¥—Ä–æ–º–æ–º
        if seq[i:] == seq[i:][::-1]:
            # –ï—Å–ª–∏ —Ö–≤–æ—Å—Ç - –ø–∞–ª–∏–Ω–¥—Ä–æ–º, –∑–Ω–∞—á–∏—Ç, –Ω–∞–º –Ω—É–∂–Ω–æ –¥–æ–±–∞–≤–∏—Ç—å
            # –ø–µ—Ä–µ–≤–µ—Ä–Ω—É—Ç—É—é "–≥–æ–ª–æ–≤—É" (–≤—Å–µ, —á—Ç–æ –±—ã–ª–æ –î–û —Ö–≤–æ—Å—Ç–∞)
            head_to_add_reversed = seq[:i][::-1]
            return len(head_to_add_reversed), head_to_add_reversed
    return 0, []
